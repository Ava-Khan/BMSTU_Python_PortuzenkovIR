import math


def calculate_function(x):
    if x < -4 or x > 10:
        return None

    if x <= -2:
        return x + 3
    elif x <= 4:
        return -0.5 * x
    elif x <= 6:
        return -2
    else:  # 6 < x <= 10
        return math.sqrt(4 - (x - 8) ** 2) - 2


def main():
    x_start = float(input("Введите точку начала от -4 до 10: "))
    x_end = float(input("Введите точку конца от -4 до 10: "))
    step = float(input("Введите шаг: "))

    if step <= 0:
        print("Вы указали неправильный шаг")
        return

    print("+--------+------------------------+")
    print("I   X    I            Y           I")
    print("+--------+------------------------+")

    current_x = x_start
    while current_x <= x_end:
        y_value = calculate_function(current_x)
        if y_value is not None:
            print(f"I{current_x: 7.2f} I       {y_value: 7.2f}          I")
        current_x += step
    print("+--------+------------------------+")


if __name__ == "__main__":
    main()